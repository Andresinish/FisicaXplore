                        <section class="CampoElectrico">
                            <h2>Campo Electrico</h2>
                            <p>
                            Un objeto con  carga eléctrica establece un campo de fuerza alrededor de él;
                            este campo de fuerza se conoce como campo eléctrico.
                            </p>
                            <p>
                             La dirección de un campo eléctrico es lejos de una carga positiva y hacia una carga negativa: <br>
                             &nbsp;&nbsp;&nbsp;&nbsp; * El campo eléctrico E apunta en sentido contrario de las cargas positivas y hacia las cargas negativas. <br>
                             &nbsp;&nbsp;&nbsp;&nbsp; * La fuerza sobre una carga positiva (+) está en la dirección del campo eléctrico. <br>
                             &nbsp;&nbsp;&nbsp;&nbsp; * La fuerza sobre una carga negativa (-) está en la dirección opuesta del campo eléctrico. 
                             Dado que la fuerza sobre una carga positiva está en la misma dirección que el campo eléctrico, 
                            siempre se usan cargas de prueba positivas para determinar la dirección de E. L
                            as cuales son tan pequeñas que no perturban la posición de ninguna otra carga en el sistema.
                            </p>

                            <p>
                            El campo eléctrico es la cantidad de fuerza por carga, es decir: <br>
                             &nbsp;&nbsp;&nbsp;&nbsp; * Magnitud del campo eléctrico = fuerza sobre carga positiva / cantidad de carga <br>
                             &nbsp;&nbsp;&nbsp;&nbsp; * E = F/q <br>
                             &nbsp;&nbsp;&nbsp;&nbsp; * Unidades de E en el SI: N/C
                            </p>
                        </section>

                        <section class="Fuerza">
                            <h2>Fuerza eléctrica debido a un campo eléctrico</h2>
                            <p>
                            Una carga q en un campo eléctrico E experimenta una fuerza F dada por: <br>
                            &nbsp;&nbsp;&nbsp;&nbsp; *Fuerza = Cantidad de carga <br>
                            &nbsp;&nbsp;&nbsp;&nbsp; * Campo eléctrico <br>
                            &nbsp;&nbsp;&nbsp;&nbsp; *F = qE <br>
                            </p>
                            <p>
                            Como se mencionó anteriormente: una carga positiva experimenta una fuerza F en la misma dirección que E,
                            y una carga negativa experimenta una fuerza F en la dirección opuesta a E.
                            </p>
                        </section>

                        <section class="CargaPuntual">
                            <h2>Los campos eléctricos de carga puntual disminuyen con la distancia</h2>
                            <p>Al aplicar la definición del campo eléctrico, E=F/q se encuentra que la magnitud del campo es:</p> 
                            <p>&nbsp;&nbsp;&nbsp;&nbsp; *E= F/q = kq/r²</p>
                            <p>  Como puedes ver, el campo eléctrico debido a una carga puntual disminuye con el inverso del cuadrado de la distancia;
                                 esto es: con 1/r². Entonces, en general, el campo eléctrico a una distancia r de una carga puntual q tiene la siguiente
                                 magnitud.
                            </p>
                        </section>

                        <section class="MagnitudDelCampo">
                            <h2>Magnitud del campo eléctrico debido a una carga puntual</h2>
                            <p>
                            &nbsp;&nbsp;&nbsp;&nbsp; * magnitud del campo eléctrico = k(carga)/(distancia)² <br>
                            &nbsp;&nbsp;&nbsp;&nbsp; *E = kq/r² <br>
                            </p>
                            <p>El campo eléctrico debido a una carga puntual disminuye rápidamente a medida que aumenta la distancia respecto a la carga.
                               Sin embargo, en realidad el campo nunca tiende a cero. Por otra parte, 
                               el campo eléctrico aumenta a medida que la distancia se acerca a cero. En consecuencia, 
                               cuanto más cerca está de una carga eléctrica, más fuerte será su campo eléctrico.   </p>
                        </section>

                        <section class="CamposSuman">
                            <h2>Los campos eléctricos se suman</h2>
                            <p>
                            Cuando un sistema consiste en varias cargas, el campo eléctrico total se encuentra mediante superposición;
                            es decir, se calcula la suma vectorial de los campos eléctricos debidos a cada una de las cargas individuales.
                            Si las cargas de un sistema permanecen en posiciones fijas,
                            el campo eléctrico que producen se conoce como campo eléctrico estático. 
                            </p>
                        </section>

                        <SECTION class="CaracteristicasVectores">
                            <h2>Características de los vectores en el campo eléctrico</h2>
                            <p>
                            <b>1. Sentido</b> <br> 
                             Indica la orientación hacia la cual apunta la flecha del vector. <br>
                            <b>2. Direccion</b> <br>
                             Se refiere al ángulo que forma el vector con respecto a una referencia predefinida. <br>
                             <b>3. Magnitud</b> <br>
                            La magnitud de un vector representa el tamaño o la intensidad del vector, 
                             cuantificando cuánto mide en términos de unidades del campo eléctrico. <br>s
                             <b>4. Punto de aplicacion</b> <br>
                             Es el lugar específico donde se origina el vector, es decir, 
                             el punto en el espacio desde el cual se aplica la fuerza o influencia representada por el vector.
                            </p>
                            <img src="images/vector.png" aling="center">
                        </SECTION>

                        <section class="Reglas">
                            <h2>Reglas para dibujar líneas del campo eléctrico:</h2>
                            &nbsp;&nbsp;&nbsp;&nbsp;*Las líneas del campo eléctrico apuntan en la dirección del vector de campo eléctrico E en cada punto. <br>
                            &nbsp;&nbsp;&nbsp;&nbsp; *Las líneas del campo eléctrico comienzan en las cargas positivas (+) o en el infinito. <br>
                             &nbsp;&nbsp;&nbsp;&nbsp; *Las líneas del campo eléctrico terminan en las cargas negativas (-) o en el infinito. <br>
                            &nbsp;&nbsp;&nbsp;&nbsp; *Las líneas del campo eléctrico están más juntas donde E tiene mayor magnitud. <br>
                            &nbsp;&nbsp;&nbsp;&nbsp; *El número de líneas del campo eléctrico que entra o sale de una carga es proporcional a la magnitud de la carga.
                        </section>

