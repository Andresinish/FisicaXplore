                        <section class="Dipolo">
                            <h2>Dipolos Electricos</h2>
                            <p>  La combinación de carga de +q y -q en nuestra figura, sé
                            conoce como dipolo eléctrico. La carga total de un dipolo 
                            es de cero, pero dado que las cargas positiva y negativa
                            están separadas, el campo eléctrico no desaparece. En 
                            vez de ello, las líneas del campo forman bucles que son 
                            característicos de un dipolo. Los dipolos son comunes
                            en la naturaleza. Quizá el ejemplo más familiar sea la 
                            Molécula de agua, que tiene carga positiva en un extremo 
                            y carga negativa en el otro.</p>
                            <img src="images/dipolo.png" aling="center">
                        </section>

                        <section class="Placas">
                            <h2>Placas infinitas y capacitores de placas paralelas</h2>
                            <p>El campo eléctrico cerca de una gran placa (en esencia 
                            infinita) cargada como la que se muestra en la siguiente
                            imagen, es uniforme en dirección y magnitud. El campo 
                            eléctrico apunta a una sola dirección: perpendicular
                            a la placa. Lo más notable es que la magnitud
                            del campo eléctrico no depende de la distancia 
                            respecto de la placa; es la misma para todas las
                            distancias.

                            Si dos placas con cargas opuestas se colocan paralelas unas 
                            a otras y se separan por una distancia finita, el resultado es
                            un capacitor de placas paralelas. En la siguiente imagen se
                            muestra un ejemplo. El campo en este caso es uniforme
                            entre las placas y es cero a fuera de ellas. Este caso es el
                            ideal, que es exactamente cierto para placas infinitas y una
                            buena aproximación para placas grandes.</p>
                            <img src="images/placa.png" aling="center">
                            <img src="images/campo.png" aling="center">
                        </section>

                        <section class="CampoInterno">
                            <p>  La distribución de carga sobre la superficie de un conductor garantiza que el campo eléctrico en el interior del conductor
                            sea cero. Este efecto se conoce como blindaje. El blindaje ocurre ya sea que el conductor sea sólido o hueco. 
                            En relación con el blindaje está el hecho de que las líneas del campo eléctrico siempre están en contacto con un conductor
                            en ángulos rectos respecto de su superficie. Además, las líneas de campo se apiñan donde un conductor tiene una punta o una 
                            saliente puntiaguda. El resultado es un campo eléctrico intenso en una punta metálica aguda.</p>
                        </section>

                        <section class="Carga">
                            <p>
                            Una forma de cargar un objeto es tocarlo con una barra cargada. Sin embargo, hay fuerzas eléctricas que actúan a distancia y, por lo tanto, también es posible cargar un objeto sin tocarlo. La carga de un objeto sin contacto directo se conoce como carga por inducción.
                            </p>
                        </section>

          
