                        <h2>Campo electrico</h2>
                        <p class="justificar">
                            Un objeto con  carga eléctrica establece un campo de fuerza alrededor de él; este campo de fuerza se conoce como campo eléctrico. 
                        </p>

                        <p class="justificar">
                            La dirección de un campo eléctrico es lejos de una carga positiva y hacia una carga negativa:<br><br>
                                &nbsp;&nbsp;&nbsp;&nbsp; ■ El campo eléctrico E apunta en sentido contrario de las cargas positivas y hacia las cargas negativas.<br>
                                &nbsp;&nbsp;&nbsp;&nbsp; ■ La fuerza sobre una carga positiva (+) está en la dirección del campo eléctrico.<br>
                                &nbsp;&nbsp;&nbsp;&nbsp; ■ La fuerza sobre una carga negativa (-) está en la dirección opuesta del campo eléctrico.<br><br>
                            Dado que la fuerza sobre una carga positiva está en la misma dirección que el campo eléctrico, siempre se usan cargas de prueba positivas para determinar la dirección de E. Las cuales son tan pequeñas que no perturban la posición de ninguna otra carga en el sistema.
                        </p>

                        <p class="justificar">
                            El campo eléctrico es la cantidad de fuerza por carga, es decir:<br>
                                &nbsp;&nbsp;&nbsp;&nbsp; ■ Magnitud del campo eléctrico = fuerza sobre carga positiva / cantidad de carga<br>
                                &nbsp;&nbsp;&nbsp;&nbsp; ■ E = F/q<br>
                                &nbsp;&nbsp;&nbsp;&nbsp; ■ Unidades de E en el SI: N/C
                        </p>

                        <p class = "centrar">
