                        <h2>Fuerza eléctrica debido a un campo eléctrico</h2>
                        <p class="justificar">
                            Una carga q en un campo eléctrico E experimenta una fuerza F dada por:<br>
                            &nbsp;&nbsp;&nbsp;&nbsp; ■ Fuerza = Cantidad de carga * Campo eléctrico<br>
                            &nbsp;&nbsp;&nbsp;&nbsp; ■ F = qE
                        </p>

                        <p class="justificar">
                            Como se mencionó anteriormente: una carga positiva experimenta una fuerza F en la misma dirección que E, y una carga negativa experimenta una fuerza F en la dirección opuesta a E.
                        </p>
